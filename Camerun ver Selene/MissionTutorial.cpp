#include "MissionTutorial.h"


//=============================================================================
//コンストラクタ
//=============================================================================
//[input]
//	pTarget:ターゲットとなるオブジェクト
//=============================================================================
CMissionTutorial::CMissionTutorial( CDraw3DObject *pTarget )
{
	m_MissionName = "チュートリアルミッション";
	m_pTarget = pTarget;	
	m_ClearPoint = 50;
	m_FilmNum = 10;
	m_LimitTime = -1;
	m_IsGameClear = false;
}

//=============================================================================
//デストラクタ
//=============================================================================
CMissionTutorial::~CMissionTutorial(void)
{
	SAFE_DELETE( m_pTarget );
}
